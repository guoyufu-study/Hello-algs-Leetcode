import java.util.List;

/**
 * 1424. 对角线遍历 II
 * 
 * <p>模拟遍历过程
 */
class Solution {
    public int[] findDiagonalOrder(List<List<Integer>> nums) {
    	// 边界处理
    	if(nums==null || nums.size()==0)
    		return new int[0];
    	
    	int count = 0;
    	// 构造答案
		int[] ans = new int[count];
		
		return ans;
    }
}